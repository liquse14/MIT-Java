package study.chap07.field_polymorphism;

public class HANKookTire extends Tire {

	public HANKookTire(String location, int maxRotation) {
		super(location,maxRotation);
	}
	
	//자식 객체에서 부모의 메소드를 재정의
	@Override
	public boolean roll() {
		++acculatedRotation;
		if(acculatedRotation<maxRotation) {//계속 사용 가능
			System.out.println(location+"HanKookTire 수명:"+(maxRotation-acculatedRotation));
		}else {
			System.out.println("***"+location+"HanKookTire펑크***");
			return false;
		}
		return false;
	}
}
